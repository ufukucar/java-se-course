package com.ufukucar;

public class Loop_03_while_do_while {

    public static void main(String[] args) {


        int i = 0;

        while ( i < 8 ) {
            System.out.println(i);
            i++;
        }


        System.out.println("------------------------------------");


        int j  = 0;
        do {

            System.out.println(j);
            j++;
        } while(j < 8 );



    }


}
