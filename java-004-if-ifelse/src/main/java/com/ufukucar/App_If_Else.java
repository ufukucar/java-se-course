public class App_If_Else {
    public static void main(String[] args) {

        int sayi = 3;

        if ( 5 < sayi ) {
            System.out.println("Benim sayım: " + sayi);
        }

        System.out.println("-----------------------------");


        int yas = 20;

        if ( 18 <= yas ) {
            System.out.println("Sağlıyor, Yaş durumu: " + yas);
        }else {
            System.out.println("Sağlamıyor, yaş: ");
        }





    }
}